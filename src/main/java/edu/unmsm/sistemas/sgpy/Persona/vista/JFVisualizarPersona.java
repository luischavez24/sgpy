    private void tablaPersonaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaPersonaMouseClicked
        int fila = tablaPersona.rowAtPoint(evt.getPoint());
        Persona P=aux.get(fila);
        if (P.getBytesLeidos()!=null){
            ImageIcon promo = new ImageIcon(P.getBytesLeidos());
            ImageIcon icono = new ImageIcon(promo.getImage().getScaledInstance(labelFotoPersona.getWidth(), labelFotoPersona.getHeight(), Image.SCALE_DEFAULT));
            labelFotoPersona.setIcon(icono);
        }
        textAreaDescripcion.setEditable(true);
        textAreaDescripcion2.setEditable(true);
        textAreaHobby.setEditable(true);
        textAreaObservaciones.setEditable(true);
        textCargo.setEditable(true);
        textCentroTrabajo.setEditable(true);
        textDNI.setEditable(true);
        textDepartamento.setEditable(true);
        textDireccion.setEditable(true);
        textDistrito.setEditable(true);
        textFechaCIP.setEditable(true);
        textLicencia.setEditable(true);
        textLicenciaVigencia.setEditable(true);
        textNacimiento.setEditable(true);
        textNroCIP.setEditable(true);
        textProvincia.setEditable(true);
        textTipoPersona.setEditable(true);
        textAreaDescripcion.setText(P.getDesPersona());
        textAreaDescripcion2.setText(P.getDesCorta());
        textAreaHobby.setText(P.getHobby());
        textAreaObservaciones.setText(P.getObservac());
        textCargo.setText(P.getCargo());
        textCentroTrabajo.setText(perDAO.descripcion(P.getCentroTrabajo()));
        textDNI.setText(P.getDNI());
        textDepartamento.setText(dep.busquedaDepartamento(P.getCodDpto()));
        textDireccion.setText(P.getDirecc());
        textDistrito.setText(dist.busquedaDistrito(P.getCodDpto(), P.getCodProv(), P.getCodDist()));
        textFechaCIP.setText(devolverFecha(P.getFecCIPVig()));
        if(P.getLicCond().equals("0")){
            textLicencia.setText("No posee");
        }
        else{
            textLicencia.setText("Posee");
        }      
        if(P.getVigente()==null){
            textLicenciaVigencia.setText("");
        }
        else{
            if (P.getVigente().equals("1")) {
                textLicenciaVigencia.setText("Vigente");
            } else {
                textLicenciaVigencia.setText("No vigente");
            }
        }
        textNacimiento.setText(devolverFecha(P.getFecNac()));
        textNroCIP.setText(P.getNroCIP());
        textProvincia.setText(prov.busquedaProvincia(P.getCodDpto(), aux.get(fila).getCodProv()));
        if(P.getTipPersona().equals("1")){
            textTipoPersona.setText("Jurídica");
        }
        else{
            textTipoPersona.setText("Natural");
        } 
        textAreaDescripcion.setEditable(false);
        textAreaDescripcion2.setEditable(false);
        textAreaHobby.setEditable(false);
        textAreaObservaciones.setEditable(false);
        textCargo.setEditable(false);
        textCentroTrabajo.setEditable(false);
        textDNI.setEditable(false);
        textDepartamento.setEditable(false);
        textDireccion.setEditable(false);
        textDistrito.setEditable(false);
        textFechaCIP.setEditable(false);
        textLicencia.setEditable(false);
        textLicenciaVigencia.setEditable(false);
        textNacimiento.setEditable(false);
        textNroCIP.setEditable(false);
        textProvincia.setEditable(false);
        textTipoPersona.setEditable(false);
        listaPersonaTics = perTics.busqueda(aux.get(fila).getCodPers());
        listaTics = tics.listTics();
        if (listaPersonaTics != null) {
            modelo2 = new DefaultTableModel();
            String[] cabecera = {"Código TIC", "Nombre", "Descripción"};
            String[][] datos = new String[listaPersonaTics.size()][3];
            for (int i = 0; i < listaPersonaTics.size(); i++) {
                datos[i][0] = String.valueOf(listaPersonaTics.get(i).getCodTics());
                for (int j = 0; j < listaTics.size(); j++) {
                    if (listaTics.get(j).getCodTics() == listaPersonaTics.get(i).getCodTics()) {
                        datos[i][1] = String.valueOf(listaTics.get(j).getDesTics());
                    }
                }
                datos[i][2] = String.valueOf(listaPersonaTics.get(i).getDesMedio());
            }
            modelo2 = new DefaultTableModel(datos, cabecera);
            tablaPersonaTics.setModel(modelo2);
        }
    }//GEN-LAST:event_tablaPersonaMouseClicked

