    private void tablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClienteMouseClicked
        int fila = tablaCliente.rowAtPoint(evt.getPoint());
        ImageIcon promo = new ImageIcon(listaCliente.get(fila).getBytesLeidos());
        ImageIcon icono = new ImageIcon(promo.getImage().getScaledInstance(labelFotoCliente.getWidth(), labelFotoCliente.getHeight(), Image.SCALE_DEFAULT));
        labelFotoCliente.setIcon(icono);
        codCliente = listaCliente.get(fila).getCodCli();
        corEmp = listaCliente.get(fila).getCorEmp();
        textRuc.setText(listaCliente.get(fila).getNroRuc());
        textAreaDescripPliego.setText(listaCliente.get(fila).getDesPliego());
        textGerGener.setText(pers.descripcion2(listaCliente.get(fila).getGerGener()));
        textGerInfraet.setText(pers.descripcion2(listaCliente.get(fila).getGerInfraest()));
        listaPersonaTics = perTics.busqueda(listaCliente.get(fila).getCodCli());
        listaTics = tics.listTics();
        if (listaPersonaTics != null) {
            modelo2 = new DefaultTableModel();
            String[] cabecera = {"Código TIC", "Nombre", "Descripción"};
            String[][] datos = new String[listaPersonaTics.size()][3];
            for (int i = 0; i < listaPersonaTics.size(); i++) {
                datos[i][0] = String.valueOf(listaPersonaTics.get(i).getCodTics());
                for (int j = 0; j < listaTics.size(); j++) {
                    if (listaTics.get(j).getCodTics() == listaPersonaTics.get(i).getCodTics()) {
                        datos[i][1] = String.valueOf(listaTics.get(j).getDesTics());
                    }
                }
                datos[i][2] = String.valueOf(listaPersonaTics.get(i).getDesMedio());
            }
            modelo2 = new DefaultTableModel(datos, cabecera);
            tablaClienteTics.setModel(modelo2);
        }
    }//GEN-LAST:event_tablaClienteMouseClicked

