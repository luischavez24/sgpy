    private void jBRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRegistrarActionPerformed
        String grado="";
        if(jTFieldCodigoR.getText().equals("")){
            jTFieldCodigoR.setBackground(Color.red);
        }else{
            jTFieldCodigoR.setBackground(Color.WHITE);
        }

        if(jTFieldNombreR.getText().equals("")){
            jTFieldNombreR.setBackground(Color.red);
        }else{
            jTFieldNombreR.setBackground(Color.WHITE);
        }

        if(  (jComboBoxGradoR.getSelectedIndex()==0 || jComboBoxGradoR.getSelectedIndex()==-1)){
            jComboBoxGradoR.setBackground(Color.red);
        }else{
            jComboBoxGradoR.setBackground(Color.WHITE);
        }

        if((jComboBoxAreaR.getSelectedIndex()==0 || jComboBoxAreaR.getSelectedIndex()==-1)){
            jComboBoxAreaR.setBackground(Color.red);
        }else{
            jComboBoxAreaR.setBackground(Color.WHITE);
        }

        if(jTFieldCodigoR.getText().equals("") || jTFieldNombreR.getText().equals("") ||
            (jComboBoxGradoR.getSelectedIndex()==0 || jComboBoxGradoR.getSelectedIndex()==-1) ||
            (jComboBoxAreaR.getSelectedIndex()==0 || jComboBoxAreaR.getSelectedIndex()==-1) ){
            JOptionPane.showMessageDialog(null,"Complete campos señalados","Advertencia",JOptionPane.WARNING_MESSAGE);
        }else{
            if(jTFieldCodigoR.getText().length()!=5){
                JOptionPane.showMessageDialog(null,"Tamaño de codigo no valido","Advertencia",JOptionPane.WARNING_MESSAGE);
                jTFieldCodigoR.setBackground(Color.red);
                jTFieldCodigoR.setText("");
            }else{
                jTFieldCodigoR.setBackground(Color.WHITE);
                jTFieldNombreR.setBackground(Color.WHITE);
                jComboBoxGradoR.setBackground(Color.WHITE);
                jComboBoxAreaR.setBackground(Color.WHITE);
                pr.setCodigo_Profesion(Integer.parseInt(jTFieldCodigoR.getText()));
                grado = retornarSiglaGrado(jComboBoxGradoR.getSelectedItem().toString());
                pr.setGrado_Profesion(grado);
                pr.setNombre_Profesion(jTFieldNombreR.getText());
                pr.setArea(jComboBoxAreaR.getSelectedItem().toString());
                pr.setVigente("1");
                prd.insertar(pr);
                JOptionPane.showMessageDialog(null,"Profesion Registrada","Registrar",JOptionPane.INFORMATION_MESSAGE);
                jTFieldCodigoR.setText("");
                jTFieldNombreR.setText("");
                jComboBoxGradoR.setSelectedIndex(0);
                jComboBoxAreaR.setSelectedIndex(0);
            }
        }

    }//GEN-LAST:event_jBRegistrarActionPerformed

