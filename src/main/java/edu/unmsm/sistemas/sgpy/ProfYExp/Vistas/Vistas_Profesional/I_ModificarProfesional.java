    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtBuscar.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Llene el codigo a Buscar", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                pr = BuscarProfesional(txtBuscar.getText());
            } catch (Exception ex) {
                Logger.getLogger(I_ModificarProfesional.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtBuscar.setText("");
            if (pr != null) {

                btnModificar.setEnabled(true);
                txtNombrePersona.setEditable(true);
                jComboBoxEmpresa.setEditable(true);
                jComboBoxProfesion.setEditable(true);
                txtCIP.setEditable(true);
                txtUniversidad.setEditable(true);
                txtNombrePersona.setText(pr.getNomPers());
                jComboBoxEmpresa.setSelectedItem(pr.getNomEmpresa());
                jComboBoxProfesion.setSelectedItem(pr.getDesProf());
                txtCIP.setText(pr.getNroCIP());
                txtUniversidad.setText(pr.getUniversidad());

            } else {
                JOptionPane.showMessageDialog(rootPane, "PERSONA NO ENCONTRADA", "NOTIFICACI√ìN", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (txtCIP.getText().equals("")) {
            txtCIP.setBackground(Color.red);
        } else {
            txtCIP.setBackground(Color.WHITE);
        }

        if (txtUniversidad.getText().equals("")) {
            txtUniversidad.setBackground(Color.red);
        } else {
            txtUniversidad.setBackground(Color.WHITE);
        }

        if ((jComboBoxProfesion.getSelectedIndex() == 0 || jComboBoxProfesion.getSelectedIndex() == -1)) {
            jComboBoxProfesion.setBackground(Color.red);
        } else {
            jComboBoxProfesion.setBackground(Color.WHITE);
        }

        if ((jComboBoxEmpresa.getSelectedIndex() == 0 || jComboBoxEmpresa.getSelectedIndex() == -1)) {
            jComboBoxEmpresa.setBackground(Color.red);
        } else {
            jComboBoxEmpresa.setBackground(Color.WHITE);
        }

        if (txtCIP.getText().equals("") || txtUniversidad.getText().equals("")
                || (jComboBoxEmpresa.getSelectedIndex() == 0 || jComboBoxEmpresa.getSelectedIndex() == -1)
                || (jComboBoxProfesion.getSelectedIndex() == 0 || jComboBoxProfesion.getSelectedIndex() == -1)) {
            JOptionPane.showMessageDialog(null, "Complete campos se√±alados", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            txtCIP.setBackground(Color.WHITE);
            txtUniversidad.setBackground(Color.WHITE);
            jComboBoxEmpresa.setBackground(Color.WHITE);
            jComboBoxProfesion.setBackground(Color.WHITE);
            pr.setNomPers(txtNombrePersona.getText());
            pr.setNomEmpresa(jComboBoxEmpresa.getSelectedItem().toString());
            pr.setDesProf(jComboBoxProfesion.getSelectedItem().toString());
            pr.setNroCIP(txtCIP.getText());
            pr.setUniversidad(txtUniversidad.getText());
            pr.setVigente("1");
            prf.Modificar(pr);
            JOptionPane.showMessageDialog(null, "Profesional Modificado", "Registrar", JOptionPane.INFORMATION_MESSAGE);
            txtCIP.setText("");
            txtUniversidad.setText("");
            txtNombrePersona.setText("");
            jComboBoxEmpresa.setSelectedIndex(0);
            jComboBoxProfesion.setSelectedIndex(0);
            txtNombrePersona.setEditable(false);
            jComboBoxEmpresa.setEditable(false);
            jComboBoxProfesion.setEditable(false);
            txtCIP.setEditable(false);
            txtUniversidad.setEditable(false);
            btnModificar.setEnabled(false);

        }
    }//GEN-LAST:event_btnModificarActionPerformed

