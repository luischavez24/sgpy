    public void llenarComboProfesion() throws Exception {
        rs = prof.listarPorVigencia("1");
        String descripcion;
        while (rs.next()) {
            descripcion = rs.getString(3);
            jComboBoxProfesion.addItem(descripcion);
        }
    }

    public void llenarComboEmpresa() throws SQLException {
        rs = per.listarPersonaCliente();
        while (rs.next()) {
            jComboBoxEmpresa.addItem(rs.getString(1));
        }
    }

    public Profesional BuscarProfesional(String codigo) throws Exception {
        boolean bandera = false;
        Profesional pf = new Profesional();
        rs = prf.BuscarProfesional(codigo);
        while (rs.next()) {
            pf.setNomPers(rs.getString(1));
            pf.setNomEmpresa(rs.getString(2));
            pf.setDesProf(rs.getString(3));
            pf.setNroCIP(rs.getString(4));
            pf.setUniversidad(rs.getString(5));
            bandera = true;
        }

        if (!bandera) {
            return null;
        } else {
            return pf;
        }

    }

