    public String retornarSiglaGrado(String grado){
        String nomGrado="";
        switch(grado){
            case "T√©cnico" : nomGrado="T";
                              break; 
            case "Bachiller":nomGrado="B";
                              break; 
            case "T√≠tulo Profesional":nomGrado="P";
                                      break; 
            case "Maestr√≠a":nomGrado="M";
                                      break; 
            case "Doctorado":nomGrado="D";
                                      break; 
            case "Post-Doctorado":nomGrado="H";
                                      break; 
        }
        return nomGrado;
    }
    
