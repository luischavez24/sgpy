    private void jBHabilitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBHabilitarActionPerformed
    int fselec= tabla_Profesion.getSelectedRow();
    if(fselec==-1){
               JOptionPane.showMessageDialog(null,"Debe seleccionar un profesion","Advertencia",JOptionPane.WARNING_MESSAGE);
    }else{
         String nombGrado;
         pr.setCodigo_Profesion(Integer.parseInt(tabla_Profesion.getValueAt(fselec, 0).toString() ));
         nombGrado=retornarSiglaGrado( tabla_Profesion.getValueAt(fselec, 1).toString() );
         pr.setGrado_Profesion(nombGrado);
         pr.setNombre_Profesion( tabla_Profesion.getValueAt(fselec, 2).toString() );
         pr.setArea( tabla_Profesion.getValueAt(fselec, 3).toString() );
         pr.setVigente("1");
         prd.Modificar(pr);
         JOptionPane.showMessageDialog(null,"Profesion habilitada","Eliminar",JOptionPane.INFORMATION_MESSAGE);   
         jBHabilitar.setVisible(false);
   }       
    }//GEN-LAST:event_jBHabilitarActionPerformed

