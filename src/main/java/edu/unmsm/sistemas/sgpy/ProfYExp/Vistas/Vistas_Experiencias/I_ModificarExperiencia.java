    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        if (txtLugarTrabajo1.getText().equals("")) {
            txtLugarTrabajo1.setBackground(Color.red);
        } else {
            txtLugarTrabajo1.setBackground(Color.WHITE);
        }

        if (txtFechaInicio1.getText().equals("")) {
            txtFechaInicio1.setBackground(Color.red);
        } else {
            txtFechaInicio1.setBackground(Color.WHITE);
        }

        if (txtFechaFin1.getText().equals("")) {
            txtFechaFin1.setBackground(Color.red);
        } else {
            txtFechaFin1.setBackground(Color.WHITE);
        }

        if (txtCargo1.getText().equals("")) {
            txtCargo1.setBackground(Color.red);
        } else {
            txtCargo1.setBackground(Color.WHITE);
        }

        if (txtDescripcion1.getText().equals("")) {
            txtDescripcion1.setBackground(Color.red);
        } else {
            txtDescripcion1.setBackground(Color.WHITE);
        }

        if (txtMotivoRetiro1.getText().equals("")) {
            txtMotivoRetiro1.setBackground(Color.red);
        } else {
            txtMotivoRetiro1.setBackground(Color.WHITE);
        }

        if ((jComboBoxProfesion.getSelectedIndex() == 0 || jComboBoxProfesion.getSelectedIndex() == -1)) {
            jComboBoxProfesion.setBackground(Color.red);
        } else {
            jComboBoxProfesion.setBackground(Color.WHITE);
        }

        if (txtLugarTrabajo1.getText().equals("") || txtFechaInicio1.getText().equals("")
                || txtFechaFin1.getText().equals("") || txtCargo1.getText().equals("")
                || txtDescripcion1.getText().equals("") || txtMotivoRetiro1.getText().equals("")
                || (jComboBoxProfesion.getSelectedIndex() == 0 || jComboBoxProfesion.getSelectedIndex() == -1)) {
            JOptionPane.showMessageDialog(null, "Complete campos señalados", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            txtLugarTrabajo1.setBackground(Color.WHITE);
            txtFechaInicio1.setBackground(Color.WHITE);
            txtFechaFin1.setBackground(Color.WHITE);
            txtDescripcion1.setBackground(Color.WHITE);
            txtCargo1.setBackground(Color.WHITE);
            txtMotivoRetiro1.setBackground(Color.WHITE);
            jComboBoxProfesion.setBackground(Color.WHITE);

            ex.setDescripcionprof(jComboBoxProfesion.getSelectedItem().toString());
            ex.setCargo(txtCargo1.getText());
            ex.setLugar_trabajo(txtLugarTrabajo1.getText());
            ex.setDestrabaho(txtDescripcion1.getText());
            ex.setMotivoretiro(txtMotivoRetiro1.getText());
            
            ex.setFecIni(txtFechaInicio1.getText());
            ex.setFecFin(txtFechaFin1.getText());
            ex.setNompers(txtNombre.getText());
            ex.setCorrexp(jSpinner.getValue().toString());
            ex.setVigente("1");

            exp.Modificar(ex);
            JOptionPane.showMessageDialog(null, "Experiencia Modificada", "Registrar", JOptionPane.INFORMATION_MESSAGE);
            txtLugarTrabajo1.setText("");
            txtFechaInicio1.setText("");
            txtFechaFin1.setText("");
            txtDescripcion1.setText("");
            txtCargo1.setText("");
            txtMotivoRetiro1.setText("");
            jComboBoxProfesion.setSelectedIndex(0);
        }

    }//GEN-LAST:event_btnModificarActionPerformed

