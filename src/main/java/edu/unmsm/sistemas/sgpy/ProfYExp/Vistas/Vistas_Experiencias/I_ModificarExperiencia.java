    public void llenarComboProfesion() throws Exception {
        rs = prof.listarPorVigencia("1");
        String descripcion;
        while (rs.next()) {
            descripcion = rs.getString(3);
            jComboBoxProfesion.addItem(descripcion);
        }
    }

    public Experiencia BuscarExperiencia(int codigo, String corrExp) throws Exception {
        boolean bandera = false;
        Experiencia e = new Experiencia();
        rs = exp.BuscarExperiencia(codigo, corrExp);
        System.out.println(rs);
        while (rs.next()) {
            e.setNompers(rs.getString(1));
            e.setCorrexp(rs.getString(2));
            e.setLugar_trabajo(rs.getString(3));
            e.setFecIni(rs.getString(4));
            e.setFecFin(rs.getString(5));
            e.setDestrabaho(rs.getString(6));
            e.setMotivoretiro(rs.getString(7));
            e.setCargo(rs.getString(8));
            e.setDescripcionprof(rs.getString(9));
            e.setVigente(rs.getString(10));
            bandera = true;
        }

        if (!bandera) {
            return null;
        } else {
            return e;
        }

    }
    
    public String devolverFecha(String fecha) {
        String f = "" + fecha.charAt(8)+fecha.charAt(9) + "/" +fecha.charAt(5)+fecha.charAt(6) + "/" + fecha.charAt(2)+fecha.charAt(3);
        return f;
    }

