    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Buscar Proyecto segÃºn cÃ³digo
        String codPyto=T1.getText();
        if(!codPyto.equals("")){
            boolean existe=backend.verificarExistenciaProyecto(codPyto);
            modelo.setRowCount(0);  //Limpia la tabla
            if(existe){
                if(Tipo!='m'){  //Si se va a agregar o cancelar
                    try {
                        Object[] Proyecto= backend.buscarProyecto(codPyto);
                        Calendar Cal = new GregorianCalendar();Cal.setTimeInMillis(Long.parseLong(String.valueOf(Proyecto[2])));
                        Proyecto[2]=Cal.get(Calendar.DAY_OF_MONTH)+"/"+(Cal.get(Calendar.MONTH)+1)+"/"+Cal.get(Calendar.YEAR);
                        Proyecto[3]=formateador.format(Double.parseDouble(String.valueOf(Proyecto[3].toString())));
                        Proyecto[4]=formateador.format(Double.parseDouble(String.valueOf(Proyecto[4].toString())));
                        modelo.setRowCount(0);  //Limpia la tabla
                        modelo.addRow(Proyecto);
                    } catch (SQLException ex) {
                        Logger.getLogger(BuscarFrm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else{   //Si se va a modificar
                    try {
                        Object[] Proyecto= backend.buscarProyectoModificar(codPyto);
                        Calendar Cal = new GregorianCalendar();Cal.setTimeInMillis(Long.parseLong(String.valueOf(Proyecto[2])));
                        Proyecto[2]=Cal.get(Calendar.DAY_OF_MONTH)+"/"+(Cal.get(Calendar.MONTH)+1)+"/"+Cal.get(Calendar.YEAR);
                        Proyecto[3]=formateador.format(Double.parseDouble(String.valueOf(Proyecto[3].toString())));
                        Proyecto[4]=formateador.format(Double.parseDouble(String.valueOf(Proyecto[4].toString())));
                        modelo.setRowCount(0);  //Limpia la tabla
                        modelo.addRow(Proyecto);
                    } catch (SQLException ex) {
                        Logger.getLogger(BuscarFrm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            else{
                //No existe el Proyecto
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Buscar Proyecto segÃºn nombre
        String nombrePyto=T2.getText();
        try {
            if(Tipo!='m'){  //Si se va a agregar o cancelar
                String Proyectos= backend.buscarProyectoNombre(nombrePyto);
                modelo.setRowCount(0);  //Limpia la tabla
                if(!Proyectos.equals("")){
                    String[] Proyecto=Proyectos.split("-");
                    int tam=Proyecto.length;
                    for(int i=0;i<tam;i++){
                        String[] aux=Proyecto[i].split("#");
                        Calendar Cal = new GregorianCalendar();Cal.setTimeInMillis(Long.parseLong(String.valueOf(aux[2])));
                        aux[2]=Cal.get(Calendar.DAY_OF_MONTH)+"/"+(Cal.get(Calendar.MONTH)+1)+"/"+Cal.get(Calendar.YEAR);
                        //System.out.println(aux);
                        aux[3]=formateador.format(Double.parseDouble(String.valueOf(aux[3].toString())));
                        aux[4]=formateador.format(Double.parseDouble(String.valueOf(aux[4].toString())));
                        modelo.addRow(aux);
                    }
                }
                else{
                    //No hay ningun proyecto con ese nombre
                }
            }
            else{   //Si se va a modificar
                String Proyectos= backend.buscarProyectoNombreModificar(nombrePyto);
                modelo.setRowCount(0);  //Limpia la tabla
                if(!Proyectos.equals("")){
                    String[] Proyecto=Proyectos.split("-");
                    int tam=Proyecto.length;
                    for(int i=0;i<tam;i++){
                        String[] aux=Proyecto[i].split("#");
                        Calendar Cal = new GregorianCalendar();Cal.setTimeInMillis(Long.parseLong(String.valueOf(aux[2])));
                        aux[2]=Cal.get(Calendar.DAY_OF_MONTH)+"/"+(Cal.get(Calendar.MONTH)+1)+"/"+Cal.get(Calendar.YEAR);
                        //System.out.println(aux);
                        aux[3]=formateador.format(Double.parseDouble(String.valueOf(aux[3].toString())));
                        aux[4]=formateador.format(Double.parseDouble(String.valueOf(aux[4].toString())));
                        modelo.addRow(aux);
                    }
                }
                else{
                    //No hay ningun proyecto con ese nombre
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(BuscarFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
  
    }//GEN-LAST:event_jButton2ActionPerformed

