    private void agregarNiveles(int codfase) throws SQLException{
        cajaNivel.removeAllItems();
        String[] Nivel=Logica.leerNiveles(codfase).split("#"); //Deben existir al menos 1 nivel
        int tam=Nivel.length;
        
        for(int i=0;i<tam;i++)
            cajaNivel.addItem((i+1)+".- "+Nivel[i]);
    }
