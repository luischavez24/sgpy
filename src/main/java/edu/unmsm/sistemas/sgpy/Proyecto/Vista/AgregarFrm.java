    private boolean comprobarDouble(String numero){
        try{
            Double.parseDouble(numero);
            return true;
        }catch(Exception e){
            return false;
        }
    }
    private String ObtenerEntero3Cifras(int numero){
        int tam= Integer.toString(numero).length();
        switch (tam){
            case 1: return "00"+Integer.toString(numero);
            case 2: return "0"+Integer.toString(numero);
            case 3: return Integer.toString(numero);
        }
        return null;
    }
    private String ObtenerEntero4Cifras(int numero){
        int tam= Integer.toString(numero).length();
        switch (tam){
            case 1: return "000"+Integer.toString(numero);
            case 2: return "00"+Integer.toString(numero);
            case 3: return "0"+Integer.toString(numero);
            case 4: return Integer.toString(numero);
        }
        return null;
    }
    private int procesarFase(int codnivel,int codfase){
        switch(codfase){
            case 1:             //Fase preinversion tiene niveles 1 y 2
                return codnivel;
            case 2:
                return codnivel+2;    //Fase inversion tiene niveles 3 y 4
            case 3:
                return codnivel+4;    //Fase posinversion tiene niveles 5 y 6
            default:
                return -1;
        }
    }
    
    
