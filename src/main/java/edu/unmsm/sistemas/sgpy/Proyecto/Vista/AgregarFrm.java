    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codPyto=T1.getText();
        String codSNIP=T2.getText();
        String nombPyto=T3.getText();
        java.sql.Date fecReg=new java.sql.Date(dateBox1.getSelectedDate().getTimeInMillis());
        String costoPIP=T4.getText().replaceAll(",", "");
        String costoDIR=T5.getText().replaceAll(",", "");
        
        
        int codFuncion=cajaFuncion.getSelectedIndex()+1;                               //No puede ser Nulo
        int codEstado=cajaEstado.getSelectedIndex()+1;                                //No puede ser Nulo
        int codFase=cajaFase.getSelectedIndex()+1;
        int codNivel=cajaNivel.getSelectedIndex()+1;                                 //No puede ser Nulo
        codNivel=procesarFase(codNivel,codFase);                                //Obtiene el nivel respectivo
        
        //System.out.println("Cod. de Nivel, Funcion, Estado y Fase: "+codNivel+codFuncion+codEstado+codFase);
        String codDpto=ObtenerEntero3Cifras(jCBDepartamento.getSelectedIndex()+1);
        String codProv=ObtenerEntero3Cifras(jCBProvincia.getSelectedIndex()+1);
        String codDist=ObtenerEntero3Cifras(jCBDistrito.getSelectedIndex()+1);
        String nombCliente=String.valueOf(cajaCliente.getSelectedItem());   //No puede ser Nulo
        String nombEsp=String.valueOf(cajaEspecialista.getSelectedItem());       //No puede ser Nulo
        String nombResp=String.valueOf(cajaResponsable.getSelectedItem());       //No puede ser Nulo
        String Observac=jTextArea1.getText().replaceAll("\n", " ");
        Observac= (Observac.equals(""))?" ":Observac;
        String Vigente=(jCheckBox1.isSelected())?"1":"0";
        if(!nombPyto.equals("") && !costoPIP.equals("") && !costoDIR.equals("") && !codDpto.equals("000") && !codProv.equals("000") && !codDist.equals("000"))
            if(comprobarDouble(costoPIP) || comprobarDouble(costoDIR)){
                
            try {Logica.guardarProyecto(codPyto,codFase,codNivel,codSNIP, nombPyto, fecReg, Double.parseDouble(costoPIP), Double.parseDouble(costoDIR), codEstado, ObtenerEntero4Cifras(codFuncion), codDpto, codProv, codDist, nombCliente, nombEsp, nombResp,Observac, Vigente);
                this.dispose();
                new OpcionesFrm().setVisible(true);
            } 
            catch (SQLException ex) {Logger.getLogger(AgregarFrm.class.getName()).log(Level.SEVERE, null, ex);}
            
                
            }
            else
                JOptionPane.showMessageDialog(this, "Los Costos de PIP y DIR no son adecuados");
        else
            JOptionPane.showMessageDialog(this, "Le falta llenar algun campo.");
    }//GEN-LAST:event_jButton1ActionPerformed

