    private void FormatoNumero(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_FormatoNumero
        char entrada=evt.getKeyChar();
        
        javax.swing.JTextField aux=(javax.swing.JTextField)evt.getComponent();
        
        if(Character.isDigit(entrada) || entrada=='.'){
            String txt=aux.getText();
            if(entrada!='.'){
                txt=txt.replaceAll(",", "");
                double valor=Double.parseDouble(txt);
                aux.setText(formateador.format(valor));
            } 
        }
        else{
            evt.consume();
        }
    }//GEN-LAST:event_FormatoNumero

